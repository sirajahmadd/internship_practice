
public class GetDemo{
  private int rno;
  private String name;
  private char sec;

  //setters
  
void setRno(int roll){
  this.rno=roll;
}
void setName(String s){
  this.name=s;
}
  void setSec(char section){
    this.sec=section;
  }

// getters
  int getRno(){
    return this.rno;
  }
  String getName(){
    return this.name;
  }
  char getSec(){
    return this.sec;
  }
  
}
