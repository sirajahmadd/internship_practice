import java.util.*;
class ZtoA{
  public static void main(String args[]){
    Scanner sc=new Scanner(System.in);
    char ch;
    System.out.println("the alphabets are:");
    ch='Z';
    System.out.println("alphabets in reverse order:");
    while(ch>='A'){
      System.out.println(ch);
    ch--;
    }
  }
}